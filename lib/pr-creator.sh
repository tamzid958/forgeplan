#!/usr/bin/env bash
# forgeplan — lib/pr-creator.sh
# PR creation: GitHub, GitLab, Bitbucket. Rollback support.

# Globals set by this module:
#   PR_URL — web URL of the created (or existing) PR

PR_URL=""

# ==========================================================================
# Token resolution — use CLI auth (gh, glab) or fallback to env
# ==========================================================================

# ---------------------------------------------------------------------------
# _resolve_git_token <host_type>
# Resolve auth token from CLI tools, falling back to GIT_HOST_TOKEN env var.
# ---------------------------------------------------------------------------
_resolve_git_token() {
  local host_type="$1"

  case "$host_type" in
    github)
      # gh auth token (GitHub CLI)
      if command -v gh > /dev/null 2>&1; then
        local token
        token=$(gh auth token 2>/dev/null || echo "")
        if [[ -n "$token" ]]; then
          echo "$token"
          return 0
        fi
      fi
      ;;
    gitlab)
      # glab auth (GitLab CLI)
      if command -v glab > /dev/null 2>&1; then
        local token
        token=$(glab auth status -t 2>&1 | grep -oP 'Token: \K\S+' || echo "")
        if [[ -n "$token" ]]; then
          echo "$token"
          return 0
        fi
      fi
      ;;
  esac

  # Fallback to env var
  echo "${GIT_HOST_TOKEN:-}"
}

# ==========================================================================
# PR body builder
# ==========================================================================

# ---------------------------------------------------------------------------
# pr_build_body <wp_id> <result> <changed_files>
# Construct PR body markdown.
# ---------------------------------------------------------------------------
pr_build_body() {
  local wp_id="$1"
  local result="$2"
  local changed_files="$3"

  local files_list=""
  if [[ -n "$changed_files" ]]; then
    files_list=$(echo "$changed_files" | sed 's/^/- `/' | sed 's/$/`/')
  fi

  local notes="- This PR was auto-generated from OpenProject work package #${wp_id}.
- Review carefully before merging."

  if [[ "$result" == "PARTIAL" ]]; then
    notes+=$'\n'"- ⚠️ Validation failed. See commit message for details."
  fi

  cat <<EOF
## Auto-generated by forgeplan

**OpenProject WP:** #${wp_id} — [View in OpenProject](${OP_BASE_URL}/work_packages/${wp_id})
**Result:** ${result}
**Generated by:** ${CLAUDE_MODEL:-unknown}

### Files Changed
${files_list:-No files listed.}

### Notes
${notes}
EOF
}

# ==========================================================================
# Platform dispatchers
# ==========================================================================

# ---------------------------------------------------------------------------
# pr_create <wp_id> <wp_subject> <result> <branch_name> <changed_files> <repo_slug> <host_type> <base_branch>
# Dispatch to platform-specific PR creation.
# ---------------------------------------------------------------------------
pr_create() {
  local wp_id="$1"
  local wp_subject="$2"
  local result="$3"
  local branch_name="$4"
  local changed_files="$5"
  local repo_slug="${6:-}"
  local host_type="${7:-}"
  local base="${8:-main}"

  if [[ -z "$repo_slug" ]]; then
    log_error "Cannot determine repo slug from git remote. Check your layer's path."
    return 1
  fi

  if [[ -z "$host_type" ]]; then
    log_error "Cannot determine git host type from remote URL."
    return 1
  fi

  # Resolve auth token from CLI tools or env
  GIT_HOST_TOKEN="$(_resolve_git_token "$host_type")"
  if [[ -z "$GIT_HOST_TOKEN" ]]; then
    log_info "Skipping PR creation (no auth token — run 'gh auth login' or 'glab auth login')"
    return 0
  fi

  # Set for platform-specific functions
  GIT_HOST_REPO="$repo_slug"

  # Resolve reviewers from config
  if [[ -n "${FP_LAYERS_JSON:-}" ]]; then
    REVIEWERS=$(echo "$FP_LAYERS_JSON" | jq -r '.reviewers // [] | join(",")' 2>/dev/null || echo "")
  fi

  local title
  case "$result" in
    PARTIAL) title="[WIP] feat(WP-${wp_id}): ${wp_subject}" ;;
    *)       title="feat(WP-${wp_id}): ${wp_subject}" ;;
  esac

  local body
  body=$(pr_build_body "$wp_id" "$result" "$changed_files")

  case "${host_type}" in
    github)   pr_create_github "$title" "$body" "$branch_name" "$base" "$wp_id" ;;
    gitlab)   pr_create_gitlab "$title" "$body" "$branch_name" "$base" "$wp_id" ;;
    bitbucket) pr_create_bitbucket "$title" "$body" "$branch_name" "$base" "$wp_id" ;;
    *)
      log_error "Unknown git host type: ${host_type}. Supported: github, gitlab, bitbucket"
      return 1
      ;;
  esac
}

# ==========================================================================
# GitHub
# ==========================================================================

# ---------------------------------------------------------------------------
# pr_create_github <title> <body> <branch> <base> <wp_id>
# ---------------------------------------------------------------------------
pr_create_github() {
  local title="$1"
  local body="$2"
  local branch="$3"
  local base="$4"
  local wp_id="$5"

  local api_url="https://api.github.com/repos/${GIT_HOST_REPO}"

  # Create PR
  local pr_body
  pr_body=$(jq -n -c \
    --arg title "$title" \
    --arg body "$body" \
    --arg head "$branch" \
    --arg base "$base" \
    '{title: $title, body: $body, head: $head, base: $base}')

  local response http_code
  local tmp_body
  tmp_body=$(mktemp)
  http_code=$(curl --silent --show-error --max-time 30 \
    -w '%{http_code}' \
    -o "$tmp_body" \
    -H "Authorization: Bearer ${GIT_HOST_TOKEN}" \
    -H "Accept: application/vnd.github+json" \
    -X POST \
    --data "$pr_body" \
    "${api_url}/pulls" 2>/dev/null) || true
  response=$(cat "$tmp_body")
  rm -f "$tmp_body"

  if [[ "$http_code" == "201" ]]; then
    PR_URL=$(echo "$response" | jq -r '.html_url')
    local pr_number
    pr_number=$(echo "$response" | jq -r '.number')
    log_info "PR created: ${PR_URL}"

    # Add reviewers
    _github_add_reviewers "$api_url" "$pr_number"
    # Add labels
    _github_add_labels "$api_url" "$pr_number" "$wp_id"
    return 0
  fi

  # Handle 422 — PR already exists
  if [[ "$http_code" == "422" ]]; then
    log_warn "PR may already exist. Searching..."
    _github_find_existing_pr "$api_url" "$branch"
    return 0
  fi

  log_error "GitHub PR creation failed (HTTP ${http_code}): $(echo "$response" | jq -r '.message // empty')"
  return 1
}

_github_add_reviewers() {
  local api_url="$1"
  local pr_number="$2"

  if [[ -z "${REVIEWERS:-}" ]]; then
    return 0
  fi

  local reviewers_json
  reviewers_json=$(echo "$REVIEWERS" | tr ',' '\n' | jq -R . | jq -sc '{reviewers: .}')

  curl --silent --max-time 15 \
    -H "Authorization: Bearer ${GIT_HOST_TOKEN}" \
    -H "Accept: application/vnd.github+json" \
    -X POST \
    --data "$reviewers_json" \
    "${api_url}/pulls/${pr_number}/requested_reviewers" > /dev/null 2>&1 || true

  log_debug "Reviewers requested: ${REVIEWERS}"
}

_github_add_labels() {
  local api_url="$1"
  local pr_number="$2"
  local wp_id="$3"

  local labels_json
  labels_json=$(jq -n -c --arg wp "wp-${wp_id}" '{"labels": ["auto-generated", $wp]}')

  curl --silent --max-time 15 \
    -H "Authorization: Bearer ${GIT_HOST_TOKEN}" \
    -H "Accept: application/vnd.github+json" \
    -X POST \
    --data "$labels_json" \
    "${api_url}/issues/${pr_number}/labels" > /dev/null 2>&1 || true

  log_debug "Labels added: auto-generated, wp-${wp_id}"
}

_github_find_existing_pr() {
  local api_url="$1"
  local branch="$2"

  local response
  response=$(curl --silent --max-time 15 \
    -H "Authorization: Bearer ${GIT_HOST_TOKEN}" \
    -H "Accept: application/vnd.github+json" \
    "${api_url}/pulls?head=${GIT_HOST_REPO%%/*}:${branch}&state=open" 2>/dev/null) || true

  local existing_url
  existing_url=$(echo "$response" | jq -r '.[0].html_url // empty')

  if [[ -n "$existing_url" ]]; then
    PR_URL="$existing_url"
    log_warn "PR already exists: ${PR_URL}. New commit pushed to existing PR."
  else
    log_warn "Could not find existing PR for branch ${branch}"
  fi
}

