# ---------------------------------------------------------------------------
# pr_create_gitlab <title> <body> <branch> <base> <wp_id>
# ---------------------------------------------------------------------------
pr_create_gitlab() {
  local title="$1"
  local body="$2"
  local branch="$3"
  local base="$4"
  local wp_id="$5"

  # URL-encode project path
  local encoded_repo
  encoded_repo=$(printf '%s' "$GIT_HOST_REPO" | jq -sRr @uri)

  local api_url="https://gitlab.com/api/v4/projects/${encoded_repo}"

  local mr_body
  mr_body=$(jq -n -c \
    --arg title "$title" \
    --arg desc "$body" \
    --arg source "$branch" \
    --arg target "$base" \
    --arg labels "auto-generated,wp-${wp_id}" \
    '{title: $title, description: $desc, source_branch: $source, target_branch: $target, labels: $labels}')

  local response http_code
  local tmp_body
  tmp_body=$(mktemp)
  http_code=$(curl --silent --show-error --max-time 30 \
    -w '%{http_code}' \
    -o "$tmp_body" \
    -H "PRIVATE-TOKEN: ${GIT_HOST_TOKEN}" \
    -H "Content-Type: application/json" \
    -X POST \
    --data "$mr_body" \
    "${api_url}/merge_requests" 2>/dev/null) || true
  response=$(cat "$tmp_body")
  rm -f "$tmp_body"

  if [[ "$http_code" == "201" ]]; then
    PR_URL=$(echo "$response" | jq -r '.web_url')
    log_info "MR created: ${PR_URL}"

    if [[ -n "${REVIEWERS:-}" ]]; then
      log_warn "GitLab reviewers require numeric user IDs. Username-based assignment not supported."
    fi
    return 0
  fi

  log_error "GitLab MR creation failed (HTTP ${http_code}): $(echo "$response" | jq -r '.message // empty')"
  return 1
}

