# ---------------------------------------------------------------------------
# pr_create_bitbucket <title> <body> <branch> <base> <wp_id>
# ---------------------------------------------------------------------------
pr_create_bitbucket() {
  local title="$1"
  local body="$2"
  local branch="$3"
  local base="$4"
  local wp_id="$5"

  local api_url="https://api.bitbucket.org/2.0/repositories/${GIT_HOST_REPO}"

  # Build reviewers array
  local reviewers_json="[]"
  if [[ -n "${REVIEWERS:-}" ]]; then
    reviewers_json=$(echo "$REVIEWERS" | tr ',' '\n' | jq -R '{username: .}' | jq -sc '.')
  fi

  local pr_body
  pr_body=$(jq -n -c \
    --arg title "$title" \
    --arg desc "$body" \
    --arg source "$branch" \
    --arg dest "$base" \
    --argjson reviewers "$reviewers_json" \
    '{
      title: $title,
      description: $desc,
      source: {branch: {name: $source}},
      destination: {branch: {name: $dest}},
      reviewers: $reviewers
    }')

  local response http_code
  local tmp_body
  tmp_body=$(mktemp)
  http_code=$(curl --silent --show-error --max-time 30 \
    -w '%{http_code}' \
    -o "$tmp_body" \
    -H "Authorization: Bearer ${GIT_HOST_TOKEN}" \
    -H "Content-Type: application/json" \
    -X POST \
    --data "$pr_body" \
    "${api_url}/pullrequests" 2>/dev/null) || true
  response=$(cat "$tmp_body")
  rm -f "$tmp_body"

  if [[ "$http_code" == "201" ]]; then
    PR_URL=$(echo "$response" | jq -r '.links.html.href')
    log_info "PR created: ${PR_URL}"
    return 0
  fi

  log_error "Bitbucket PR creation failed (HTTP ${http_code}): $(echo "$response" | jq -r '.error.message // empty')"
  return 1
}

# ==========================================================================
# Rollback (I-9)
# ==========================================================================

# ---------------------------------------------------------------------------
# handle_rollback <wp_id>
# Undo a previous generation: close PR, delete branch, revert status.
# ---------------------------------------------------------------------------
handle_rollback() {
  local wp_id="$1"

  local summary_file="${LOG_DIR:-./logs}/run-summary.jsonl"
  if [[ ! -f "$summary_file" ]]; then
    log_error "No run-summary.jsonl found. Nothing to rollback."
    exit 2
  fi

  # Find the most recent entry for this WP
  local entry
  entry=$(grep "\"wp_id\":${wp_id}" "$summary_file" | tail -1)
  if [[ -z "$entry" ]]; then
    log_error "No run history found for WP #${wp_id}"
    exit 2
  fi

  local branch pr_url
  branch=$(echo "$entry" | jq -r '.branch // empty')
  pr_url=$(echo "$entry" | jq -r '.pr_url // empty')

  # Confirm with user
  echo "Rollback WP #${wp_id}:"
  [[ -n "$branch" ]] && echo "  - Delete branch: ${branch}"
  [[ -n "$pr_url" ]] && echo "  - Close PR: ${pr_url}"
  echo "  - Revert OP status"
  printf "Proceed? [y/N] "
  local answer
  read -r answer
  if [[ "$answer" != "y" && "$answer" != "Y" ]]; then
    echo "Rollback cancelled."
    return 0
  fi

  # Close PR
  if [[ -n "$pr_url" ]]; then
    rollback_close_pr "$pr_url"
  fi

  # Delete branch
  if [[ -n "$branch" ]]; then
    rollback_delete_branch "$branch"
  fi

  # Revert status
  rollback_revert_status "$wp_id"

  echo "✅ Rollback complete for WP #${wp_id}"
}

# ---------------------------------------------------------------------------
# rollback_close_pr <pr_url>
# Close/decline the PR on the hosting platform.
# ---------------------------------------------------------------------------
rollback_close_pr() {
  local pr_url="$1"

  if [[ "$pr_url" == *"github.com"* ]]; then
    # Resolve token from CLI auth
    local token
    token="$(_resolve_git_token "github")"
    if [[ -z "$token" ]]; then
      log_warn "No GitHub auth token. Close PR manually: ${pr_url}"
      return 0
    fi

    # Extract owner/repo/number from URL
    local pr_path
    pr_path=$(echo "$pr_url" | sed 's|.*github.com/||;s|/pull/|/pulls/|')
    local api_url="https://api.github.com/repos/${pr_path}"
    curl --silent --max-time 15 \
      -H "Authorization: Bearer ${token}" \
      -H "Accept: application/vnd.github+json" \
      -X PATCH \
      --data '{"state":"closed"}' \
      "$api_url" > /dev/null 2>&1 || true
    log_info "PR closed: ${pr_url}"

  elif [[ "$pr_url" == *"gitlab.com"* ]]; then
    log_warn "GitLab MR close via URL not implemented. Close manually: ${pr_url}"

  elif [[ "$pr_url" == *"bitbucket.org"* ]]; then
    log_warn "Bitbucket PR decline via URL not implemented. Decline manually: ${pr_url}"

  else
    log_warn "Unknown PR platform. Close manually: ${pr_url}"
  fi
}

# ---------------------------------------------------------------------------
# rollback_delete_branch <branch_name>
# Delete remote and local branch, checkout base.
# ---------------------------------------------------------------------------
rollback_delete_branch() {
  local branch_name="$1"

  # Delete remote branch
  git -C "$REPO_ROOT" push "${GIT_REMOTE}" --delete "${branch_name}" 2>/dev/null || {
    log_warn "Could not delete remote branch ${branch_name} (may not exist)"
  }

  # Checkout base branch
  git -C "$REPO_ROOT" checkout "${GIT_BASE_BRANCH}" --quiet 2>/dev/null || true

  # Delete local branch
  git -C "$REPO_ROOT" branch -D "${branch_name}" 2>/dev/null || {
    log_warn "Could not delete local branch ${branch_name} (may not exist)"
  }

  log_info "Branch deleted: ${branch_name}"
}

# ---------------------------------------------------------------------------
# rollback_revert_status <wp_id>
# Revert OP status to pickup_status and post comment.
# ---------------------------------------------------------------------------
rollback_revert_status() {
  local wp_id="$1"

  local pickup_id
  pickup_id=$(config_get_status_id "pickup_status")

  if [[ -n "$pickup_id" ]]; then
    op_update_wp_status "$wp_id" "$pickup_id" || true
  fi

  op_post_comment "$wp_id" "⏪ forgeplan rollback: Code generation reverted. Branch and PR deleted." || true
}
